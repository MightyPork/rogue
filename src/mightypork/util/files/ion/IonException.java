package mightypork.util.files.ion;


public class IonException extends Exception {
	
	public IonException() {
		super();
	}
	
	
	public IonException(String message, Throwable cause) {
		super(message, cause);
	}
	
	
	public IonException(String message) {
		super(message);
	}
	
	
	public IonException(Throwable cause) {
		super(cause);
	}
	
}
